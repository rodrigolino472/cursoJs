/*Operadores de comparação:
> maior que 
< menor que 
>= maior ou igual que
<= menor ou igual que
== igualdade(valor)
=== igualdade estrita(valor e tipo)
!= diferente(valor)
!== diferente estrito(valor e tipo)

*/
console.log(1 > 2);   //false
console.log(1 < 2);   //true
console.log(1 >= 2);  //false
console.log(1 <= 2);  //true
console.log(1 == 2);  //false
console.log(1 === 2); //false
console.log(1 != 2);  //true
console.log(1 !== 2); //true
